	 UPDATE tbl_stockitems set ItemCode=@ItemCode,ItemName=@ItemName,categoryID=@categoryID,
	 SubCategoryID=@SubCategory,status=@status,StockLocation=@StockLocation,
	 LastModifiedDateTime=@LastModifiedDateTime,LastModifiedBy=@LastModifiedBy,
