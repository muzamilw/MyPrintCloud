
Create PROCEDURE [dbo].[sp_StockInventory_UPDATE_STOCK]

	(
		@ItemCode varchar(50),
		@ItemName varchar(255),
		@categoryID integer,
		@SubCategory integer,
		@status integer,
		@StockLocation varchar(50),
		@LastModifiedDateTime datetime,
		@LastModifiedBy integer,
		@StockCreated datetime,
		@SupplierID integer,
		@ItemWeight integer,
		@ItemColour varchar(50),
		@ItemSizeCustom smallint,
		@ItemSizeID integer,
		@ItemSizeHeight float,
		@ItemSizeWidth float,
		@ItemSizeDim integer,
		@ItemUnitSize integer,
		@CostPrice float,
		@StockLevel integer,
		@PackageQty float,
		@ReOrderLevel float,
		@ItemCoatedType smallint,
		@ItemCoated smallint,
		@ItemExposure smallint,
		@ItemExposureTime integer,
		@ItemProcessingCharge float,
		@PerQtyRate float,
		@PerQtyQty float,
