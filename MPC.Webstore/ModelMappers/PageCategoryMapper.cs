using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using DomainModels = MPC.Models.DomainModels;
using ApiModels = MPC.Webstore.Models; 


namespace MPC.Webstore.ModelMappers
{
    public static class PageCategoryMapper
    {
        #region

        /// <summary>
        /// Crete From Web Model
        /// </summary>
        public static ApiModels.PageCategory CreateFrom(this DomainModels.PageCategory source)
        {
            return new ApiModels.PageCategory
            {
              
                CategoryId = source.CategoryId,
                CategoryName = source.CategoryName
            };
        }




        /// <summary>
        /// Crete From Web Model
        /// </summary>
        public static DomainModels.PageCategory CreateFrom(this ApiModels.PageCategory source)
        {
            return new DomainModels.PageCategory
            {
                CategoryId = source.CategoryId,
                CategoryName = source.CategoryName
            };
        }


        #endregion

    }
}